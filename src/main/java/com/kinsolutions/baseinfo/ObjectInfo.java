package com.kinsolutions.baseinfo;

public class ObjectInfo {
	
	private Integer objectId;
	private String objectType;
	private String objectName;
	private String actionType;
	private String objectStatusMessage;
	
	public Integer getObjectId() {
		return objectId;
	}
	public void setObjectId(Integer objectId) {
		this.objectId = objectId;
	}
	public String getObjectType() {
		return objectType;
	}
	public void setObjectType(String objectType) {
		this.objectType = objectType;
	}
	public String getObjectName() {
		return objectName;
	}
	public void setObjectName(String objectName) {
		this.objectName = objectName;
	}
	public String getActionType() {
		return actionType;
	}
	public void setActionType(String actionType) {
		this.actionType = actionType;
	}
	public String getObjectStatusMessage() {
		return objectStatusMessage;
	}
	public void setObjectStatusMessage(String objectStatusMessage) {
		this.objectStatusMessage = objectStatusMessage;
	}
	
	
}
